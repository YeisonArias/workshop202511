package volumen;

public class VolumenCaja {
    public static void main(String[] args) {
        //Calcular volumen de una caja con dimensiones A x B X C
        double volumen;
        double A = 10;
        double B = 20;
        double C = 30;
        volumen = A * B * C;
        System.out.println("El volumen de la caja es: " + volumen);
    }
}

public class CicloWhile {
    public static void main(String[] args) {
        boolean x = true;
        while (x) {
            System.out.println("x es true");
            x = false;
        }

        int i = 0;
        while (i <= 10) {
            System.out.println("i es " + i);
           i++;
        }
    }
}
